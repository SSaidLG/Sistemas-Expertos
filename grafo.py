    """
    Retorna un diccionario anidado con las distancias directas entre todos los pares de nodos.
    heuristica[i][j] = distancia directa de i a j (usada como h' en búsquedas heurísticas)
    Los nodos están numerados del 1 al 28.
    """
    h = {}
    for i in range(28):
        h[i + 1] = {}
        for j in range(28):
            h[i + 1][j + 1] = _TABLA_RAW[i][j]
    return h


